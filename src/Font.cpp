Font::Font() {
  glGenTextures(1, &texture_);
  glBindTexture(GL_TEXTURE_2D, texture_);
  glTexImage2D(GL_TEXTURE_2D, 0, GL_ALPHA8, kCharWidth * 16,
    kCharHeight * 8, 0, GL_ALPHA, GL_UNSIGNED_BYTE, kFontData);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_BASE_LEVEL, 0);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAX_LEVEL, 0);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_R, GL_CLAMP);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP);
  glBindTexture(GL_TEXTURE_2D, 0);
}

Font::~Font() {
  glDeleteTextures(1, &texture_);
}

void Font::Draw(const char* str, int x, int y) {
  glEnable(GL_TEXTURE_2D);
  glBindTexture(GL_TEXTURE_2D, texture_);

  glEnable(GL_BLEND);
  glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

  GLint viewport[4];
  glGetIntegerv(GL_VIEWPORT, viewport);
  const GLint width = viewport[2] - viewport[0];
  const GLint height = viewport[3] - viewport[1];

  glMatrixMode(GL_PROJECTION);
  glLoadIdentity();
  glOrtho(0, width, 0, height, -1, 1);
  glMatrixMode(GL_MODELVIEW);
  glLoadIdentity();

  glBegin(GL_QUADS);
  while (*str) {
    if (*str > 0) {
      const int row = *str / 16;
      const int column = *str % 16;
      static const auto columnWidth = 1.0f / 16.0f;
      static const auto rowHeight = 1.0f / 8.0f;
      
      glTexCoord2f(column * columnWidth, (row + 1) * rowHeight);
      glVertex2i(x, y);
      
      glTexCoord2f((column + 1) * columnWidth, (row + 1) * rowHeight);
      glVertex2i(x + kCharWidth, y);
      
      glTexCoord2f((column + 1) * columnWidth, row * rowHeight);
      glVertex2i(x + kCharWidth, y + kCharHeight);
      
      glTexCoord2f(column * columnWidth, row * rowHeight);
      glVertex2i(x, y + kCharHeight);
    }
    x += kCharWidth;
    ++str;
  }
  glEnd();

  glDisable(GL_BLEND);

  glBindTexture(GL_TEXTURE_2D, texture_);
  glDisable(GL_TEXTURE_2D);
}
